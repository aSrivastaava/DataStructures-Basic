public class EquilateralTriangle {
    public static void main(String[] args) {
        // Print equilateral triangle using * and five println

        System.out.println("   *");
        System.out.println("  ***");
        System.out.println(" *****");
        System.out.println("*******");

        // Print equilateral triangle using * and only one print

        System.out.print("   *\n  ***\n *****\n*******\n");

    }
}
