public class Loop {
    public static void main(String[] args) {

        // WAP to print 1 to 9 using while loop

        int x = 1;

        while (x <= 9) {
            System.out.println(x);
            x++;
        }
        System.out.println("Done");

        // WAP to print 1 to 9 using For loop

        for (int i = 1; i <= 9; i++)
            System.out.println(i);

        System.out.println("Done");

    }
}
