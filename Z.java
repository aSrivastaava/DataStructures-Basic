public class Z {
    public static void main(String[] args) {

        // Print Z shape using * and multiple println

        System.out.println("*****");
        System.out.println("   * ");
        System.out.println("  *  ");
        System.out.println(" *   ");
        System.out.println("*****");

        // Print Z shape using * and single print

        System.out.print("*****\n   * \n  *  \n *   \n*****");
    }
}
